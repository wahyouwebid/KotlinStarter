package kotlin_fundamental.number.number_operation

fun main() {
    val numberOne = 5
    val numberTwo = 2
    val result = numberOne + numberTwo

    println(result)

    println(numberOne + numberTwo)

    println(numberOne / numberTwo)

    println(numberOne * numberTwo)

    println(numberOne - numberTwo)

    println(numberOne % 2)

    println(1 + 2 * 3)

    println((3 + 2) * 1)

}